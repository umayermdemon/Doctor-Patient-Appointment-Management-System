import { model, Schema } from "mongoose";
import { TPatient } from "./patient.interface";

const patientSchema = new Schema<TPatient>({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
  gender: {
    type: String,
    enum: ["male", "female", "other"],
    required: true,
  },
});

export const Patient = model<TPatient>("Patient", patientSchema);
